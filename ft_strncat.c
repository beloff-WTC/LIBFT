#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*d;
	size_t	i;

	d = s1;
	i = 0;
	while (*d)
		d++;
	while (*s2 && i++ < n)
		*d++ = *s2++;
	*d = '\0';
	return (s1);
}
