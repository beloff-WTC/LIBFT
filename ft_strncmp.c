#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	if (!n)
		return (0);
	i = 1;
	while (*s1 == *s2)
	{
		if (!*s1++ || i++ == n)
			return (0);
		s2++;
	}
	return ((t_uchar)(*s1) - (t_uchar)(*s2));
}
